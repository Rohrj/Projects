				<h2> What is this project about? </h2>
				<p>Creatures that evade existance have captured the imaginations of people around the world for centuries. These creatures were eventually given the name "cryptids" which means "An animal whose existence or survival is disputed or unsubstantiated" (<a class="" href="https://en.oxforddictionaries.com/definition/cryptid" target="_blank">Oxford Dictionary</a>, 2017). Information about cryptids is scattered across libraries and the web. A central information hub is needed so that the curious can find all that they need to know about some of the worlds most famous cryptids. Users can discover where they are located, when they have been sighted last, and learn from historical accounts. This site will take the role of that hub and will detail the intricacies of what makes these elusive animals so intriguing and that they can't be forgotten.</p>
				<p>Marketing slogan: A hub to the undiscoverable.</p>
