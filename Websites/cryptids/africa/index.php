	<p>In 1932, Ivan T. Sanderson, a prominent cryptozoologist, took an expedition to the Congo. With the natives as guides, he found large tracks and witnessed a creature (larger than a hippo) slip under the river water He also heard a story from the pygmies about their own encounters with the beast. They had dammed up the river entrance to Lake Tele with a wall of spears. Two angry Mokele-Mbembe attacked the dam and the pygmies defended themselves and killed one of the creatures with a spear. In 1983, a noted zoologist who was on an expedition witnessed a large head and long neck emerge from the lake water and confirmed it was not a snake, crocodile, nor a turtle. Many expeditions took place in the Congo between 1909 and 2017, but still no solid proof has been discovered. The Mokele-Mbembe will most likely continue to evade humans for a long time since the Congo is so dense with jungle and, considering it is suggested to be a prehistoric species, there is probably very few specimans still living.</p>
