	<p>As per usual with most cryptids, westerners become aware of their possible existance much later than the natives of the region. The name Mokele-Mbembe, when translated from Lingala (the language of the indigenous peoples of the Congo basin), literally means "one that stops the flow of rivers". This massive creature has been living among the natives of the Congo for thousands of years. The most common physical description of this dinosaur is that it is as large as an elephant and resembles a sauropod (long neck, large body, dinosaur-like). The pygmies living in the area have stories that say the Mokele-Mbembe is an herbivore and eats mainly vines. </p>
