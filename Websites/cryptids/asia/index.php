	<p>It seems that almost every continent has an ape-like, bipedal creature roaming in its secluded areas because, like Bigfoot and the Ucu, Yetis are large humanoids that are covered in hair and have huge feet. Sightings of this cryptid are rare due to the remote area in which they dwell, but explorers as far back as 79 AD have described human-like creatures living in the mountains and have found multitudes of footprints in the snow. Physical evidence is also much more prominent when it comes to Yetis. Scalps, hands, and hair samples have been collected over the years but the local monasteries rarely allow scientists to run tests and take DNA samples.</p>
	<p>Just because they are rare, doesn't mean credible sightings never happen. In 1925, N.A. Tombazi, a photographer and member of the Royal Geographical Society, saw a Yeti at around 15,000 feet on the mountains. According to Tombazi, he observed the creature from about 200 or 300 yards away for about one minute. He wrote, "Unquestionably, the figure in outline was exactly like a human being, walking upright and stopping occasionally to pull at some dwarf rhododendron bushes. It showed up dark against the snow, and as far as I could make out, wore no clothes." About two hours later, Tombazi and his companions descended the mountain, and saw what they took to be the creature's prints, described as "similar in shape to those of a man, but only six to seven inches long by four inches wide.... The prints were undoubtedly those of a biped."</p>
