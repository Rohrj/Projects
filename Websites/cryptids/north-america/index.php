<article id="bigfoot" class="radius boxshadow light-blue-na dark-bg-na">

	<p>Sasquatch, Bigfoot, Ape-man, Monkey Man, The Bad Smelling Tree Man. Just some of the names these North American cryptid are called. Sightings of these beasts have occurred all over the Pacific North West, Canada, California, the Rocky Mountains, the
		Great Lakes region, and some of the Northeast and Southern states. They are large, hairy creatures and descriptions from people who think they have seen them are all very similar. According to reports, Sasquatch are 7 to 9 feet tall, bipedal, and ape-like.
		They have broad shoulders, large eyes, pronounced brows, and they smell terrible. Their feet leave 15 to 18 inch long tracks.</p>
	<p>Native Americans have many stories about giant, hairy animals that date back centuries, but the first documented sighting was in the Pacific Northwest in 1924. It was said that some miners were attacked at their cabin by some Sasquatch and one was even
		shot and killed. Large tracks were found and considerable damage to the cabin was reported. For the next hundred years, reports have continued to pop up. Some of them are fake, but the most convincing all have certain aspects in common. They all happen
		in places secluded from humans (forests, near rivers and lakes, national parks), describe similar creatures and their distinctive stench, and, of course, the cameras used to snap photos are blurry and outdated. Sasquatch excells at being found by the
		people who are not looking for it and who are unprepared for the situation. Soon, with technology and smartphones being a part of everyones lives, a clear image will surface that will show Sasquatch in all its glory.</p>

